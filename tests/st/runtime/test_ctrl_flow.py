class TestForIfElseNested(PTOTestCase):
    """Test for loop with if-else nested inside.

    This test demonstrates the variable scoping issue when if-else is nested
    inside a for loop. The problem: each loop iteration creates new variable
    names (out_0, out_1, out_2...) in different branches, and the return
    statement cannot find the correct variable after the loop.

    Issue: Variable scoping in for loops with if-else branches.
    """

    __test__ = False

    def get_name(self) -> str:
        return "for_if_else_nested"

    def define_tensors(self) -> list[TensorSpec]:
        return [
            TensorSpec("a", [256, 64], DataType.FP32, init_value=2.0),
            TensorSpec("b", [256, 64], DataType.FP32, init_value=3.0),
            TensorSpec("c", [256, 64], DataType.FP32, is_output=True),
        ]

    def get_program(self) -> Any:
        @pl.program
        class ForIfElseNestedProgram:
            @pl.function(type=pl.FunctionType.InCore)
            def kernel_loop_with_if(
                self,
                a: pl.Tensor[[256, 64], pl.FP32],
                b: pl.Tensor[[256, 64], pl.FP32],
                c: pl.Out[pl.Tensor[[256, 64], pl.FP32]],
                use_add: pl.Scalar[pl.BOOL],
            ) -> pl.Tensor[[256, 64], pl.FP32]:
                # If-else INSIDE the for loop - this causes variable scoping issues
                for i in pl.range(4):
                    offset_i = i * 64
                    tile_a: pl.Tile[[64, 64], pl.FP32] = pl.load(a, [offset_i, 0], [64, 64])
                    tile_b: pl.Tile[[64, 64], pl.FP32] = pl.load(b, [offset_i, 0], [64, 64])

                    # Problem: Each iteration creates out_0, out_1, out_2, out_3
                    # in different branches, causing variable scoping issues
                    if use_add:
                        tile_result: pl.Tile[[64, 64], pl.FP32] = pl.add(tile_a, tile_b)
                        out: pl.Tensor[[256, 64], pl.FP32] = pl.store(tile_result, [offset_i, 0], [64, 64], c)
                    else:
                        tile_result: pl.Tile[[64, 64], pl.FP32] = pl.mul(tile_a, tile_b)
                        out: pl.Tensor[[256, 64], pl.FP32] = pl.store(tile_result, [offset_i, 0], [64, 64], c)

                # After loop: which 'out' to return? out_0 from iteration 0? out_3 from iteration 3?
                return out

            @pl.function(type=pl.FunctionType.Orchestration)
            def orchestrator(
                self,
                a: pl.Tensor[[256, 64], pl.FP32],
                b: pl.Tensor[[256, 64], pl.FP32],
            ) -> pl.Tensor[[256, 64], pl.FP32]:
                c: pl.Tensor[[256, 64], pl.FP32] = pl.create_tensor([256, 64], dtype=pl.FP32)
                use_add = False
                c = self.kernel_loop_with_if(a, b, c, use_add)
                return c

        return ForIfElseNestedProgram

    def compute_expected(self, tensors, params=None):
        tensors["c"][:] = tensors["a"] * tensors["b"]


