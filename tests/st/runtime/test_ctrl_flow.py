"""
Tests for control flow code generation (scf.for and scf.if) using PyPTO frontend.

This module validates that scf.for and scf.if are correctly generated by the PTO codegen,
ensuring correct code generation and execution for loop and conditional patterns.

Test Coverage:
    - For loops with tile operations (add, mul) ✅
    - If-else with scalar boolean conditions inside InCore functions ✅
    - Both CCE and PTO backend variants ✅
    - For+if-else nested pattern (marked as XFAIL due to known issue) ⚠️

Key Requirements for If-Else in InCore Functions:
    - Use pl.Scalar[pl.BOOL] for condition parameters (not INT64)
    - Both branches must use the SAME variable names for assignments
    - Avoid if-else inside for loops due to variable scoping issues (see XFAIL tests)
    - Pattern based on kernel_online_update from paged_attention_example.py

Known Issue - For Loop with Nested If-Else:
    When if-else is nested inside a for loop, the SSA transformation creates different
    variable names for each iteration (out_0, out_1, out_2, ...) in different branches.
    After the loop completes, the return statement cannot determine which variable to use.

    Example of the problem:
        for i in range(4):
            if condition:
                out: Tensor = store(...)  # Becomes out_0, out_1, out_2, out_3
            else:
                out: Tensor = store(...)  # Becomes out_0, out_1, out_2, out_3
        return out  # Which out? out_0 or out_3?

    Workaround: Place if-else outside the for loop, or use separate kernels for each branch.
    See TestForIfElseNestedXFAIL for a minimal reproduction case.
"""

from typing import Any

import pypto.language as pl
import pytest
from harness.core.harness import DataType, PTOTestCase, TensorSpec
from pypto.backend import BackendType
from pypto.ir.pass_manager import OptimizationStrategy


class TestForLoopAdd(PTOTestCase):
    """Test tile add inside a for loop (1 iteration).

    Validates scf.for code generation. The loop runs once,
    performing a simple tile add. Expected result: c = a + b.
    """

    __test__ = False

    def get_name(self) -> str:
        return "for_loop_add_64x64"

    def define_tensors(self) -> list[TensorSpec]:
        return [
            TensorSpec("a", [256, 64], DataType.FP32, init_value=2.0),
            TensorSpec("b", [256, 64], DataType.FP32, init_value=3.0),
            TensorSpec("c", [256, 64], DataType.FP32, is_output=True),
        ]

    def get_program(self) -> Any:
        @pl.program
        class ForLoopAddProgram:
            @pl.function(type=pl.FunctionType.InCore)
            def kernel_add_loop(
                self,
                a: pl.Tensor[[256, 64], pl.FP32],
                b: pl.Tensor[[256, 64], pl.FP32],
                c: pl.Out[pl.Tensor[[256, 64], pl.FP32]],
            ) -> pl.Tensor[[256, 64], pl.FP32]:
                for i in pl.range(4):
                    offset_i = i * 64
                    tile_a: pl.Tile[[64, 64], pl.FP32] = pl.load(a, [offset_i, 0], [64, 64])
                    tile_b: pl.Tile[[64, 64], pl.FP32] = pl.load(b, [offset_i, 0], [64, 64])
                    tile_c: pl.Tile[[64, 64], pl.FP32] = pl.add(tile_a, tile_b)
                    out: pl.Tensor[[256, 64], pl.FP32] = pl.store(tile_c, [offset_i, 0], [64, 64], c)
                return out

            @pl.function(type=pl.FunctionType.Orchestration)
            def orchestrator(
                self,
                a: pl.Tensor[[256, 64], pl.FP32],
                b: pl.Tensor[[256, 64], pl.FP32],
            ) -> pl.Tensor[[256, 64], pl.FP32]:
                c: pl.Tensor[[256, 64], pl.FP32] = pl.create_tensor([256, 64], dtype=pl.FP32)
                c = self.kernel_add_loop(a, b, c)
                return c

        return ForLoopAddProgram

    def compute_expected(self, tensors, params=None):
        tensors["c"][:] = tensors["a"] + tensors["b"]


