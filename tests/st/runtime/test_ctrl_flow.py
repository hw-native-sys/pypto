class TestIfElseInCore(PTOTestCase):
    """Test if-else inside InCore function with scalar condition.

    Based on the pattern from kernel_online_update in paged_attention_example.py.
    Key: both branches must assign to the same output variables.
    """

    __test__ = False

    def get_name(self) -> str:
        return "if_else_incore"

    def define_tensors(self) -> list[TensorSpec]:
        return [
            TensorSpec("a", [64, 64], DataType.FP32, init_value=2.0),
            TensorSpec("b", [64, 64], DataType.FP32, init_value=3.0),
            TensorSpec("c", [64, 64], DataType.FP32, is_output=True),
        ]

    def get_program(self) -> Any:
        @pl.program
        class IfElseInCoreProgram:
            @pl.function(type=pl.FunctionType.InCore)
            def kernel_conditional(
                self,
                a: pl.Tensor[[64, 64], pl.FP32],
                b: pl.Tensor[[64, 64], pl.FP32],
                c: pl.Out[pl.Tensor[[64, 64], pl.FP32]],
                use_add: pl.Scalar[pl.BOOL],
            ) -> pl.Tensor[[64, 64], pl.FP32]:
                tile_a: pl.Tile[[64, 64], pl.FP32] = pl.load(a, [0, 0], [64, 64])
                tile_b: pl.Tile[[64, 64], pl.FP32] = pl.load(b, [0, 0], [64, 64])

                # Both branches must use the SAME variable names
                if use_add:
                    tile_result: pl.Tile[[64, 64], pl.FP32] = pl.add(tile_a, tile_b)
                    out: pl.Tensor[[64, 64], pl.FP32] = pl.store(tile_result, [0, 0], [64, 64], c)
                else:
                    tile_result: pl.Tile[[64, 64], pl.FP32] = pl.sub(tile_a, tile_b)
                    out: pl.Tensor[[64, 64], pl.FP32] = pl.store(tile_result, [0, 0], [64, 64], c)

                return out

            @pl.function(type=pl.FunctionType.Orchestration)
            def orchestrator(
                self,
                a: pl.Tensor[[64, 64], pl.FP32],
                b: pl.Tensor[[64, 64], pl.FP32],
            ) -> pl.Tensor[[64, 64], pl.FP32]:
                c: pl.Tensor[[64, 64], pl.FP32] = pl.create_tensor([64, 64], dtype=pl.FP32)
                use_add = True  # Use add operation
                c = self.kernel_conditional(a, b, c, use_add)
                return c

        return IfElseInCoreProgram

    def compute_expected(self, tensors, params=None):
        # use_add = 1, so we do addition
        tensors["c"][:] = tensors["a"] + tensors["b"]


